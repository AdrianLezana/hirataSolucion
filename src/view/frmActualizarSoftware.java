    private void tblActualizacionSoftwareMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblActualizacionSoftwareMouseClicked

        int fila = tblActualizacionSoftware.getSelectedRow();

        if (fila >= 0) {
            txtIdActualizacion.setText(tblActualizacionSoftware.getValueAt(fila, 0).toString());

            String nombreEquipo = tblActualizacionSoftware.getValueAt(fila, 1).toString();
            seleccionarEquipo(nombreEquipo);

            String nombreSoftware = tblActualizacionSoftware.getValueAt(fila, 2).toString();
            seleccionarSoftware(nombreSoftware);

            lblVersionSoftware.setText("Versión: "
                    + tblActualizacionSoftware.getValueAt(fila, 3).toString());

            txtFecha.setText(tblActualizacionSoftware.getValueAt(fila, 4).toString());
            txtaDetalles.setText(tblActualizacionSoftware.getValueAt(fila, 5).toString());
        }

    }//GEN-LAST:event_tblActualizacionSoftwareMouseClicked

