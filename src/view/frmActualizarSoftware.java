    private void seleccionarEquipo(String nombre) {
        for (int i = 0; i < cmbNombreEquipo.getItemCount(); i++) {
            EquipoCombo e = cmbNombreEquipo.getItemAt(i);
            if (e.toString().equals(nombre)) {
                cmbNombreEquipo.setSelectedIndex(i);
                break;
            }
        }
    }

    private void seleccionarSoftware(String nombre) {
        for (int i = 0; i < cmbNombreSoftware.getItemCount(); i++) {
            SoftwareCombo s = cmbNombreSoftware.getItemAt(i);
            if (s.toString().equals(nombre)) {
                cmbNombreSoftware.setSelectedIndex(i);
                break;
            }
        }
    }

    public void limpiarCampos() {

        txtaDetalles.setText("");
        txtFecha.setText("");
        txtIdActualizacion.setText("");

        cmbNombreSoftware.setSelectedItem(null);
        cmbNombreEquipo.setSelectedItem(null);

        lblVersionSoftware.setText("Version: ");
    }

    private void cargarTabla() {

        ActualizacionSoftwareService service = new ActualizacionSoftwareService();
        List<Object[]> lista = service.listar();

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{
            "ID", "Equipo", "Software", "Versión", "Fecha", "Descripción"
        });

        for (Object[] fila : lista) {
            modelo.addRow(fila);
        }

        tblActualizacionSoftware.setModel(modelo);
    }

    private void cargarSoftware() {
        SoftwareService service = new SoftwareService();
        List<SoftwareCombo> lista = service.obtenerSoftwareCombo();

        cmbNombreSoftware.removeAllItems();

        for (SoftwareCombo s : lista) {
            cmbNombreSoftware.addItem(s);
        }
    }

    private void cargarEquipos() {
        EquipoService service = new EquipoService();
        List<EquipoCombo> lista = service.obtenerEquiposCombo();

        cmbNombreEquipo.removeAllItems();

        for (EquipoCombo e : lista) {
            cmbNombreEquipo.addItem(e);
        }
    }

