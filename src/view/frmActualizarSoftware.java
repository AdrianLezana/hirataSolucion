    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        try {
            ActualizacionSoftware a = new ActualizacionSoftware();

            // Equipo
            EquipoCombo eq = (EquipoCombo) cmbNombreEquipo.getSelectedItem();
            a.setIdEquipo(eq.getId());

            // Software
            SoftwareCombo sw = (SoftwareCombo) cmbNombreSoftware.getSelectedItem();
            a.setIdSoftware(sw.getId());

            a.setFecha(txtFecha.getText());
            a.setDescripcion(txtaDetalles.getText());

            ActualizacionSoftwareService service = new ActualizacionSoftwareService();

            if (service.registrar(a)) {
                JOptionPane.showMessageDialog(this, "Actualización registrada");
                cargarTabla();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al registrar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        try {
            ActualizacionSoftware a = new ActualizacionSoftware();

            a.setIdActualizacion(Integer.parseInt(txtIdActualizacion.getText()));

            EquipoCombo eq = (EquipoCombo) cmbNombreEquipo.getSelectedItem();
            a.setIdEquipo(eq.getId());

            SoftwareCombo sw = (SoftwareCombo) cmbNombreSoftware.getSelectedItem();
            a.setIdSoftware(sw.getId());

            a.setFecha(txtFecha.getText());
            a.setDescripcion(txtaDetalles.getText());

            ActualizacionSoftwareService service = new ActualizacionSoftwareService();

            if (service.actualizar(a)) {
                JOptionPane.showMessageDialog(this, "Actualización actualizada");
                cargarTabla();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

        try {
            int id = Integer.parseInt(txtIdActualizacion.getText());

            ActualizacionSoftwareService service = new ActualizacionSoftwareService();

            if (service.eliminar(id)) {
                JOptionPane.showMessageDialog(this, "Actualización eliminada");
                cargarTabla();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_btnBorrarActionPerformed

