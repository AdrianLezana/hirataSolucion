    public void limpiarCampos() {

        txtEquipoID.setText("");
        txtNombreEquipo.setText("");
        cmbEstado.setSelectedItem(null);
        cmbTipo.setSelectedItem(null);
        txtMarca.setText("");
        txtModelo.setText("");

    }

    public boolean validarDatos() {
        return (txtNombreEquipo.getText().isEmpty() || cmbTipo.getSelectedItem().toString().isEmpty() || txtMarca.getText().isEmpty() || txtModelo.getText().isEmpty() || cmbEstado.getSelectedItem().toString().isEmpty());
    }
    
    public void validacionCorrecta(){
        if (validarDatos()) {
            JOptionPane.showMessageDialog(this, "Faltan datos.");
            return;
        }
    }

