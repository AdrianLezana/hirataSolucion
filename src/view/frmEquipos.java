    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        /*if (validarDatos()) {
            JOptionPane.showMessageDialog(this, "Faltan datos.");
            return;
        }*/
        
        validacionCorrecta();
         

        Equipo eq = new Equipo();

        eq.setNombre(txtNombreEquipo.getText());
        eq.setTipo(cmbTipo.getSelectedItem().toString());
        eq.setMarca(txtMarca.getText());
        eq.setModelo(txtModelo.getText());
        eq.setEstado(cmbEstado.getSelectedItem().toString());

        EquipoService service = new EquipoService();

        if (service.registrarEquipo(eq)) {
            JOptionPane.showMessageDialog(this, "Equipo registrado");
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }

        limpiarCampos();
        cargarTabla();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (validarDatos()) {
            JOptionPane.showMessageDialog(this, "Faltan datos.");
            return;
        }

        Equipo eq = new Equipo();

        eq.setIdEquipo(Integer.parseInt(txtEquipoID.getText()));
        eq.setNombre(txtNombreEquipo.getText());
        eq.setTipo(cmbTipo.getSelectedItem().toString());
        eq.setMarca(txtMarca.getText());
        eq.setModelo(txtModelo.getText());
        eq.setEstado(cmbEstado.getSelectedItem().toString());

        EquipoService service = new EquipoService();

        if (service.actualizarEquipo(eq)) {
            JOptionPane.showMessageDialog(this, "Equipo actualizado");
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }

        limpiarCampos();
        cargarTabla();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int id = Integer.parseInt(txtEquipoID.getText());

        EquipoService service = new EquipoService();

        if (service.eliminarEquipo(id)) {
            JOptionPane.showMessageDialog(this, "Equipo eliminado");
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }

        limpiarCampos();
        cargarTabla();
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void tblEquiposRegistradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEquiposRegistradosMouseClicked

        int fila = tblEquiposRegistrados.getSelectedRow();

        if (fila >= 0) {
            txtEquipoID.setText(tblEquiposRegistrados.getValueAt(fila, 0).toString());
            txtNombreEquipo.setText(tblEquiposRegistrados.getValueAt(fila, 1).toString());
            cmbTipo.setSelectedItem(tblEquiposRegistrados.getValueAt(fila, 2).toString());
            txtMarca.setText(tblEquiposRegistrados.getValueAt(fila, 3).toString());
            txtModelo.setText(tblEquiposRegistrados.getValueAt(fila, 4).toString());
            cmbEstado.setSelectedItem(tblEquiposRegistrados.getValueAt(fila, 5).toString());
        }
    }//GEN-LAST:event_tblEquiposRegistradosMouseClicked

