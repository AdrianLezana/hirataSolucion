    private void cargarTabla() {
        EquipoService service = new EquipoService();
        List<Equipo> lista = service.listarEquipos();

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{
            "ID", "Nombre", "Tipo", "Marca", "Modelo", "Estado"
        });

        for (Equipo eq : lista) {
            modelo.addRow(new Object[]{
                eq.getIdEquipo(),
                eq.getNombre(),
                eq.getTipo(),
                eq.getMarca(),
                eq.getModelo(),
                eq.getEstado()
            });
        }

        tblEquiposRegistrados.setModel(modelo);
    }

