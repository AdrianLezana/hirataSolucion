    private void tblSoftwareMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSoftwareMouseClicked
        
        int fila = tblSoftware.getSelectedRow();
        
        if (fila >= 0) {
            txtIdSoftware.setText(tblSoftware.getValueAt(fila, 0).toString());
            txtNombreSoftware.setText(tblSoftware.getValueAt(fila, 1).toString());
            txtVersion.setText(tblSoftware.getValueAt(fila, 2).toString());
        }

    }//GEN-LAST:event_tblSoftwareMouseClicked

