    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        Software s = new Software();
        
        s.setNombre(txtNombreSoftware.getText());
        s.setVersion(txtVersion.getText());
        
        SoftwareService service = new SoftwareService();
        
        if (service.registrar(s)) {
            JOptionPane.showMessageDialog(this, "Software registrado");
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
        
        limpiarDatos();
        cargarTablaSoftware();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        if (validarDatos()) {
            JOptionPane.showMessageDialog(this, "Faltan datos.");
            return;
        }
        
        Software s = new Software();
        
        s.setNombre(txtNombreSoftware.getText());
        s.setVersion(txtVersion.getText());
        
        SoftwareService service = new SoftwareService();
        
        if (service.registrar(s)) {
            JOptionPane.showMessageDialog(this, "Software editado");
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
        
        limpiarDatos();
        cargarTablaSoftware();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        
        int id = Integer.parseInt(txtIdSoftware.getText());
        
        SoftwareService service = new SoftwareService();
        
        if (service.eliminar(id)) {
            JOptionPane.showMessageDialog(this, "Software eliminado");
        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }
        
        limpiarDatos();
        cargarTablaSoftware();

    }//GEN-LAST:event_btnBorrarActionPerformed

