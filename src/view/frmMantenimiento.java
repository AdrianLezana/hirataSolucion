    public void cargarTablaMantenimiento() {

        String sql = "SELECT m.idMantenimiento, c.marca, c.modelo, m.tipoMantenimiento, m.descripcion, m.fecha "
                + "FROM mantenimiento m "
                + "JOIN camiones c ON m.idCamion = c.idCamion";

        try {
            Conexion con = new Conexion();
            Connection conexion = con.getConnection();

            PreparedStatement ps = conexion.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            DefaultTableModel modelo = (DefaultTableModel) tblMantenimiento.getModel();
            modelo.setRowCount(0);

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("idMantenimiento"),
                    rs.getString("marca"),
                    rs.getString("modelo"),
                    rs.getString("tipoMantenimiento"),
                    rs.getString("descripcion"),
                    rs.getTimestamp("fecha")
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar tabla: " + e.getMessage());
        }
    }

