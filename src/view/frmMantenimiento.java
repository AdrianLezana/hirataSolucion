    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            // 1. Validaciones
            if (txtIdCamion.getText().isEmpty() || txtaDescripcion.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Complete los campos");
                return;
            }

            int idCamion = Integer.parseInt(txtIdCamion.getText());
            String tipo = cmbTipoMantenimiento.getSelectedItem().toString();
            String descripcion = txtaDescripcion.getText();

            // 2. Llamar al service
            RegistroMantenimientoService service = new RegistroMantenimientoService();
            service.registrarMantenimiento(idCamion, tipo, descripcion);

            JOptionPane.showMessageDialog(null, "Mantenimiento registrado correctamente");

            // 3. Refrescar tabla
            cargarTablaMantenimiento();

            // 4. Limpiar campos
            txtIdCamion.setText("");
            txtaDescripcion.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID debe ser numérico");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (idMantenimientoSeleccionado == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un registro");
            return;
        }

        String tipo = cmbTipoMantenimiento.getSelectedItem().toString();
        String descripcion = txtaDescripcion.getText();

        RegistroMantenimientoService service = new RegistroMantenimientoService();
        service.actualizarMantenimiento(idMantenimientoSeleccionado, tipo, descripcion);

        JOptionPane.showMessageDialog(null, "Actualizado correctamente");

        cargarTablaMantenimiento();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (idMantenimientoSeleccionado == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un registro");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(null,
                "żEliminar este mantenimiento?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {

            RegistroMantenimientoService service = new RegistroMantenimientoService();
            service.eliminarMantenimiento(idMantenimientoSeleccionado);

            JOptionPane.showMessageDialog(null, "Eliminado correctamente");

            cargarTablaMantenimiento();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

