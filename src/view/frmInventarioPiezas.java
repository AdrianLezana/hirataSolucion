    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        try {
            InventarioPieza p = new InventarioPieza();

            p.setIdPieza(Integer.parseInt(txtIdPieza.getText()));
            p.setNombre(txtNombre.getText());
            p.setTipo(cmbTipo.getSelectedItem().toString());
            p.setEstado(cmbEstado.getSelectedItem().toString());
            p.setDescripcion(txtaDescripcion.getText());

            InventarioService service = new InventarioService();

            if (service.actualizar(p)) {
                JOptionPane.showMessageDialog(this, "Pieza actualizada");
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

        cargarInventario();

    }//GEN-LAST:event_btnEditarActionPerformed

