    private void tblInventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblInventarioMouseClicked

        int fila = tblInventario.getSelectedRow();

        if (fila >= 0) {

            txtIdPieza.setText(tblInventario.getValueAt(fila, 0).toString());
            txtNombre.setText(tblInventario.getValueAt(fila, 1).toString());
            cmbTipo.setSelectedItem(tblInventario.getValueAt(fila, 2).toString().trim());
            txtCantidad.setText(tblInventario.getValueAt(fila, 3).toString());
            cmbEstado.setSelectedItem(tblInventario.getValueAt(fila, 4).toString().trim());
            txtaDescripcion.setText(tblInventario.getValueAt(fila, 5).toString());

            int id = Integer.parseInt(tblInventario.getValueAt(fila, 0).toString());
            cargarMovimientos(id);
        }

    }//GEN-LAST:event_tblInventarioMouseClicked

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        try {
            InventarioPieza p = new InventarioPieza();

            p.setNombre(txtNombre.getText());
            p.setTipo(cmbTipo.getSelectedItem().toString());
            p.setCantidad(Integer.parseInt(txtCantidad.getText()));
            p.setEstado(cmbEstado.getSelectedItem().toString());
            p.setDescripcion(txtaDescripcion.getText());

            InventarioService service = new InventarioService();

            if (service.registrarPieza(p)) {
                JOptionPane.showMessageDialog(this, "Pieza registrada");
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

        cargarInventario();

    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnSalidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalidaActionPerformed

        try {
            int id = Integer.parseInt(txtIdPieza.getText());

            InventarioService service = new InventarioService();

            if (service.registrarSalida(id, 1)) {
                JOptionPane.showMessageDialog(this, "Salida registrada");
            } else {
                JOptionPane.showMessageDialog(this, "Stock insuficiente");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

        cargarInventario();
        int id = Integer.parseInt(txtIdPieza.getText().toString());
        cargarMovimientos(id);

    }//GEN-LAST:event_btnSalidaActionPerformed

