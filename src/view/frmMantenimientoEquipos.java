    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        validacionCorrecta();
        
        try {
            MantenimientoEquipo m = new MantenimientoEquipo();

            m.setIdEquipo(Integer.parseInt(txtIdEquipo.getText()));

            TecnicoCombo tecnico = (TecnicoCombo) cmbTecnico.getSelectedItem();
            m.setIdTecnico(tecnico.getId());

            m.setTipoMantenimiento(cmbTipo.getSelectedItem().toString());
            m.setDescripcion(txtaDescripcion.getText());
            m.setObservaciones(txtaObservaciones.getText());
            m.setFecha(txtFecha.getText());

            MantenimientoEquipoService service = new MantenimientoEquipoService();

            if (service.registrar(m)) {
                JOptionPane.showMessageDialog(this, "Mantenimiento registrado");
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al registrar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

        cargarTablaMantenimiento();
    }//GEN-LAST:event_btnRegistrarActionPerformed

