    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

        try {
            int id = Integer.parseInt(txtIdMantenimiento.getText());

            MantenimientoEquipoService service = new MantenimientoEquipoService();

            if (service.eliminar(id)) {
                JOptionPane.showMessageDialog(this, "Mantenimiento eliminado");
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

        cargarTablaMantenimiento();
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void tblHistorialMantenimientosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHistorialMantenimientosMouseClicked
        int fila = tblHistorialMantenimientos.getSelectedRow();

        if (fila >= 0) {
            txtIdMantenimiento.setText(tblHistorialMantenimientos.getValueAt(fila, 0).toString());
            txtIdEquipo.setText(tblHistorialMantenimientos.getValueAt(fila, 1).toString());

            String nombreTecnico = tblHistorialMantenimientos.getValueAt(fila, 2).toString();
            seleccionarTecnicoEnCombo(nombreTecnico);

            cmbTipo.setSelectedItem(tblHistorialMantenimientos.getValueAt(fila, 3).toString());
            txtaDescripcion.setText(tblHistorialMantenimientos.getValueAt(fila, 4).toString());
            txtaObservaciones.setText(tblHistorialMantenimientos.getValueAt(fila, 5).toString());
            txtFecha.setText(tblHistorialMantenimientos.getValueAt(fila, 6).toString());
        }
    }//GEN-LAST:event_tblHistorialMantenimientosMouseClicked

