    public void cargarTabla() {

        String sql = "SELECT r.idRegistro, c.marca, c.modelo, r.kilometrajeRecorrido, r.fechaRegistro "
                + "FROM registroKilometraje r "
                + "JOIN camiones c ON r.idCamion = c.idCamion";

        try {
            Conexion con = new Conexion();
            Connection conexion = con.getConnection();

            PreparedStatement ps = conexion.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            DefaultTableModel modelo = (DefaultTableModel) tblRegistros.getModel();
            modelo.setRowCount(0); // limpiar tabla

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("idRegistro"),
                    rs.getString("marca"),
                    rs.getString("modelo"),
                    rs.getInt("kilometrajeRecorrido"),
                    rs.getTimestamp("fechaRegistro")
                });
            }

            

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar tabla: " + e.getMessage());
            e.printStackTrace();
        }

    }

