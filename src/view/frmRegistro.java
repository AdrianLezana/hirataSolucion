    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        try {
            // Validaciones
            if (txtIDCamion.getText().isEmpty() || txtKm.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Complete todos los campos");
                return;
            }

            int idCamion = Integer.parseInt(txtIDCamion.getText());
            int km = Integer.parseInt(txtKm.getText());

            if (km <= 0) {
                JOptionPane.showMessageDialog(null, "Kilometraje inválido");
                return;
            }

            // Llamar al service
            RegistroKilometrajeService service = new RegistroKilometrajeService();
            service.registrarKilometraje(idCamion, km);

            // Refrescar tabla
            cargarTabla();
            cargarCamiones();

            // Limpiar campos
            txtIDCamion.setText("");
            txtKm.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese números válidos");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

