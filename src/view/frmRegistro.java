    private void txtKmKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKmKeyReleased
        // Elimina caracteres que no sean números
        String limpio = txtKm.getText().replaceAll("[^0-9]", "");

        // Limita el máximo a 6 números aunque los peguen
        if (limpio.length() > 6) {
            limpio = limpio.substring(0, 6);
        }
    }//GEN-LAST:event_txtKmKeyReleased

    private void txtKmKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKmKeyTyped
        // Restringe el txtStock a sólo recibir números
        char tecla = evt.getKeyChar();
        if (tecla < '0' || tecla > '9') {
            evt.consume();
        }

        // Restringe al txtKm a recibir un máximo de 4 dígitos
        if (txtKm.getText().length() >= 6) {
            evt.consume();
        }

        // Restringe el uso del espacio dentro del txtStock
        if (Character.isWhitespace(tecla)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtKmKeyTyped

